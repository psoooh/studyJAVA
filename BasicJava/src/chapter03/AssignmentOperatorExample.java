package chapter03;

public class AssignmentOperatorExample {
	public static void main(String[] args) {
		
		int index = 0;
		
		index = index + 1;
		index += 1; // index = index +1;과 같다
		index++;// 앞에 붙을때와 뒤에 붙을때 저장되는 방식이 달라진다. 잘 구별!!!
		++index;
		
		int i = 0; //index를 i로 표현한다
		
		i = i + 1;
		i += 1; // index = index +1;과 같다
		i++;// 앞에 붙을때와 뒤에 붙을때 저장되는 방식이 달라진다. 잘 구별!!!
		++i;
		
	}
}
